CREATE TABLE ORDERS(
Order_Id INT AUTO_INCREMENT PRIMARY KEY,
CustomerId INT ,
Order_Date DATE,
FOREIGN KEY (CustomerId) REFERENCES CUSTOMERS(CustomerId));

