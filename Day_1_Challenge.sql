USE ABC_DATA;

CREATE TABLE CUSTOMERS(
CustomerId INT AUTO_INCREMENT PRIMARY KEY,
First_Name VARCHAR (100),
Last_Name VARCHAR (100),
Email VARCHAR(250) UNIQUE NOT NULL
);

